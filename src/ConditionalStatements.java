public class ConditionalStatements {
    public static void main(String[] args) {
        int num1=50, num2=30;
        if (num1==num2)
            System.out.println("both are equals");
        else if (num1 > num2)
            System.out.println(num1 + "is greater");
        else
            System.out.println(num2 +"is greater");
    }
}
